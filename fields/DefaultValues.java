public class DefaultValues{
	public static void main(String[] args)
	{
		System.out.println("invoked main");
		
		System.out.println("default of byte:"+byteValue);
		
		System.out.println("default of short:"+shortValue);
		
		System.out.println("default of int:"+integerValue);
		
		System.out.println("default of long:"+longValue);
		
		System.out.println("default of float:"+floatValue);
	
		System.out.println("default of double:"+doubleValue);
		
		System.out.println("default of boolean:"+booleanValue);
		
		System.out.println("default of char:"+characterValue);
		
		System.out.println("default of string:"+stringValue);
		
		System.out.println("EXIT :: main");	
	}
	public static byte byteValue;
	
	public static short shortValue;
	
	public static int integerValue;
	
	public static long longValue;
	
	public static float floatValue;
	
	public static double doubleValue;
	
	public static boolean booleanValue;
	
	public static char characterValue;
	
	public static String stringValue;
}